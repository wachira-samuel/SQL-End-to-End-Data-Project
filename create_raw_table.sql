disease VARCHAR(150),
treatment_cost VARCHAR(50),
insurance_cover VARCHAR(50),
mobile_money_payment VARCHAR(50),
blood_pressure VARCHAR(20),
temperature VARCHAR(20),
doctor_id VARCHAR(50),
follow_up_required VARCHAR(20)
